var app = angular.module("myApp", ['ui.router',"ngMessages","ngCookies"]);
//123
app.config(function ($stateProvider, $urlRouterProvider) {
    $urlRouterProvider.otherwise("main/");
    $stateProvider.state("main",{
        url: '/main/:mainID',///{main:[0-9]{1,8}}
        views:{
            "":{
                templateUrl:"main/index.html"
            },
            "topbar@main":{
                templateUrl:"public/topbar.html"
            },
            "left_menu@main":{
                templateUrl:"public/left_menu.html"
            },
            "main_index@main":{
                templateUrl:"main/main_index.html"
            },
            "footer@main":{
                templateUrl:"public/footer.html"
            }
        }
    }).state("main.state",{
        url:"/:ChildID",
        views:{
            "main_index@main":{
                templateUrl:"main/main_index.html"
            },
        }
    }).state("user_data",{
        url:"/userData",
        views:{
            "":{
                templateUrl:"pensonal/user_index.html"
            },
            "topbar@user_data":{
                templateUrl:"public/topbar.html"
            },
            "main@user_data":{
                templateUrl:"pensonal/user_index_data.html"
            }
        }
    })
})